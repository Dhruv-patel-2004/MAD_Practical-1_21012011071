fun main() {
    val intVariable = 42
    val doubleVariable = 3.14159
    val floatVariable  = 1.2345f
    val longVariable= 123456789L
    val shortVariable = 100
    val byteVariable= 8
    val charVariable= 'A'
    val booleanVariable = true
    val stringVariable = "Hello, Kotlin!"
    println("Integer Value: $intVariable")
    println("Double Value: $doubleVariable")
    println("Float Value: $floatVariable")
    println("Long Value: $longVariable")
    println("Short Value: $shortVariable")
    println("Byte Value: $byteVariable")
    println("Char Value: $charVariable")
    println("Boolean Value: $booleanVariable")
    println("String Value: $stringVariable")
}
